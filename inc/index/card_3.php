<h2>Водопады ручья «Руфабго»</h2>
<p>Любители активного отдыха очень хорошо знают и&nbsp;любят это&nbsp;небольшую,
но&nbsp;очень&nbsp;«строптивую» речку. И&nbsp;дело даже не&nbsp;в&nbsp;ее&nbsp;«бешеном» характере,
а&nbsp;ведь&nbsp;именно так&nbsp;переводится с&nbsp;адыгейского языка название этого
быстрого потока.&nbsp;По&nbsp;руслу реки отважных путешественников ждут
живописные водопады и&nbsp;великолепные виды на&nbsp;красоты берегов этого
стремительного чуда.</p>